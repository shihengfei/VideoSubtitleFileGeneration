public class PersonFace {
	
	/** 人脸图片链接 */
	private String url;
	
	/** 人员组名称 */
	private String name;
	
	/** 人员组 ID */
	private String personGroupId;
	
	/** 附加到人员组或人员的数据，可 null */
	private Object userData;
	
	/** 创建人时人员名称 */
	private String personName;
	
	/** 创建人后返回生成的人员ID */
	private String personId;
	
	/** 检测人像返回的人像 id */
	private String faceId;

	/** 检测人像返回的脸部属性 */
	private Object faceRectangle;
	
	/** 为人员添加人脸后返回 id */
	private String persistedFaceId;

	/** 人脸验证结果 */
	private boolean isIdentical;
	
	/** 人脸验证相似度，0-1,1相似度最高 */
	private double confidence;
	
	public double getConfidence() {
		return confidence;
	}

	public void setConfidence(double confidence) {
		this.confidence = confidence;
	}

	public boolean isIdentical() {
		return isIdentical;
	}

	public void setIdentical(boolean isIdentical) {
		this.isIdentical = isIdentical;
	}

	public String getPersistedFaceId() {
		return persistedFaceId;
	}

	public void setPersistedFaceId(String persistedFaceId) {
		this.persistedFaceId = persistedFaceId;
	}

	public Object getFaceRectangle() {
		return faceRectangle;
	}

	public void setFaceRectangle(Object faceRectangle) {
		this.faceRectangle = faceRectangle;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPersonGroupId() {
		return personGroupId;
	}

	public void setPersonGroupId(String personGroupId) {
		this.personGroupId = personGroupId;
	}

	public Object getUserData() {
		return userData;
	}

	public void setUserData(Object userData) {
		this.userData = userData;
	}

	public void setUserData(String userData) {
		this.userData = userData;
	}

	public String getPersonName() {
		return personName;
	}

	public void setPersonName(String personName) {
		this.personName = personName;
	}

	public String getPersonId() {
		return personId;
	}

	public void setPersonId(String personId) {
		this.personId = personId;
	}

	public String getFaceId() {
		return faceId;
	}

	public void setFaceId(String faceId) {
		this.faceId = faceId;
	}
	
}
